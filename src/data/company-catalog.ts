function makeSource(seed: CatalogSeed): CompanySource[] {
  return [
    {
      label: seed.sourceLabel,
      url: seed.website,
      note: seed.sourceNote,
    },
  ];
}

function makeCompany(seed: CatalogSeed): Company {
  const coords =
    companyGeocodes[seed.id] ??
    cityCoordinates[seed.city] ??
    cityCoordinates.Florida;

  return {
    id: seed.id,
    kind: seed.kind,
    name: seed.name,
    sector: seed.sector,
    address: seed.address,
    city: seed.city,
    region: seed.region,
    phone: seed.phone,
    email: seed.email,
    website: seed.website,
    coverage: seed.coverage,
    lat: coords.lat,
    lng: coords.lng,
    capabilities: seed.capabilities,
    forkliftConfirmed: seed.forkliftConfirmed ?? (seed.kind === "carrier" ? true : null),
    forkliftEvidence: seed.forkliftEvidence,
    workTypes: seed.workTypes,
    tags: seed.tags,
    fitScore: seed.fitScore,
    verification: seed.verification,
    contacts: seed.contact ? [seed.contact] : [],
    notes: seed.notes,
    opportunity: seed.opportunity,
    sources: makeSource(seed),
  };
}

